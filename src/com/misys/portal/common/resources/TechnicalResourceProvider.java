  private static String getResourceFromDB(String key)
  {
    String result = null;
    if (dbCache.containsKey(key))
    {
      if (!"notfound".equals(dbCache.get(key))) {
        result = (String)dbCache.get(key);
      }
    }
    else
    {
      ParameterKey keys = new ParameterKey();
      keys.setBrch_code("00001");
      keys.setCompany_id("*");
      keys.setParm_id("P131");
      keys.addKey("KEY_1", key);
      try
      {
        List<ParameterData> paramSet = ParameterData.retrieveAllParameterSet(keys);
        if (paramSet.size() == 1)
        {
          result = ((ParameterData)paramSet.get(0)).getData("DATA_1");
          dbCache.put(key, result);
        }
        else
        {
          dbCache.put(key, "notfound");
        }
      }
      catch (Exception ee)
      {
        if (LOG.isDebugEnabled()) {
          LOG.debug("Exception in getBindingCodeResource method in BindingCodesResourcesProvider", ee);
        }
      }
    }
    return result;
  }
  
  public static String getTechnicalResource(String key)
  {
    String resourceKey = getResourceFromDB(key);
    if (resourceKey != null) {
      return resourceKey;
    }
    try
    {
      Field field = TechnicalResourceProvider.class.getDeclaredField(key);
      if (field.getType().equals(String.class)) {
        return (String)field.get(null);
      }
    }
    catch (Exception e)
    {
      if (LOG.isWarnEnabled()) {
        LOG.warn("Key not found: " + key, e);
      }
    }
    return null;
  }
  
